#include "mem.hpp"
#include <chrono>
#include <iostream>
#include <thread>
using namespace std;
using namespace mem;

int main() {
    Process game;
    if (!game.attach(L"game.exe")) {
        cerr << "attach that bai, ma loi: " << game.lastError() << "\n";
        return 1;
    }

    uintptr_t base = game.module(L"game.exe").base();
    if (!base) {
        cerr << "khong tim thay module\n";
        return 1;
    }

    // day la offset vi du. Trong thuc te ban phai tu do ra chuoi offset nay
    // bang Cheat Engine    
    const uintptr_t ammoOffset1 = 0x2C;
    const uintptr_t ammoOffset2 = 0x14;
    const int desiredAmmo = 999;

    cout << "dang ghi de dan, nhan Ctrl+C de dung\n";

    while (game.isAttached()) {
        auto ammo = game.pointer(base)
                        .offset(ammoOffset1)
                        .offset(ammoOffset2);

        if (ammo) {
            ammo.write(desiredAmmo);
        }

        std::this_thread::sleep_for(std::chrono::milliseconds(50));
    }

    return 0;
}





